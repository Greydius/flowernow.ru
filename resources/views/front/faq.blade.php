<p><strong>Гарантии Floristum.ru</strong></p>

<p><strong>37. Безопасна ли оплата через сайт?</strong><span id="sohrm" name=""></span></p>

<p>Да, оплата совершается на отдельной странице, а данные клиентов не сохраняются. Мы сотрудничаем только с известными и надежными системами.</p>

<p><strong>38. Могу ли я не беспокоиться за сохранность денег?</strong><span id="vozvrd" name=""></span></p>

<p>Да, конечно. Оформляя заказ, вы вносите деньги сначала на счет нашего сервиса, где они сохраняются до момента завершения заказа и еще в течение трех суток после. Такая мера предпринята для того, если клиент будет иметь вопросы по заказу. За это время у вас есть возможность открыть спор, тогда деньги не поступят на счет флориста до решения всех проблем.</p>

<p><strong>39. Как я могу вернуть оплаченную сумму?</strong><span id="abuse" name=""></span></p>

<p>Мы работаем по удобной системе, сначала сумма на вашем счете не снимается, а лишь замораживается. Поэтому при оформлении возврата суммы до клиентов нашего сайта доходят очень быстро. Если ваш заказ еще не был взят в работу, то деньги вернутся мгновенно. Если заказ уже взят в работу, то придется подождать от 1 до 14 рабочих дней.</p>

<p><strong>40. Где я могу написать свою жалобу?</strong><span id="tvit" name="tvit"></span></p>

<p>Если вы остались разочарованы своим заказом, то обязательно напишите отзыв. Если он будет негативным, то вы сможете открыть спор с флористом. На время выяснения всех обстоятельств деньги на счету флориста будут заблокированы. Вы можете договориться с флористом о замене букета или о возвращении полной суммы денег. Наши флористы дорожат клиентами, поэтому часто предлагают им скидки к последующим заказам. Спор может быть начат в течение трех дней с момента доставки букета. Если вы ранее оставляли хороший отзыв по заказу, то открытие спора невозможно.</p>

<p><strong>41. Как я могу написать отзыв?</strong><span id="realtvit" name=""></span></p>

<p>После доставки цветов на ваш номер телефона придет сообщение, в ответ на которое вы можете написать ваши впечатления по работе с флористом и по самому заказу. На сайте, войдя в личный кабинет, вы можете написать отзыв.</p>

<p><strong>42. Все ли отзывы реальные?</strong><span id="thinks" name="thinks"></span></p>
<span id="bio" name="bio"></span>
<p>У каждого магазина множество отзывов, среди них встречаются как положительные, так и негативные отклики. Возле каждого из них указано, кто написал отзыв: заказчик или получатель. Отзыв может редактироваться только тем человеком, который написал его ранее.</p>

