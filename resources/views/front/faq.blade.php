<p><strong>Оплата</strong></p>

<p><strong>5. Почему я не могу произвести оплату картой?</strong><span id="garant" name="garant"></span>

<p>Удостоверьтесь, что данные вашей банковской карты введены правильно, имя и фамилия прописаны на английском языке в точности как на карте. CVV код &ndash; это 3 цифры, которые находятся на задней стороне карты. В большинстве случаев система даст вам объяснение, почему платеж не может быть успешно осуществлен. Если платеж отклоняется вашим банком, то обязательно свяжитесь со службой поддержки по бесплатному номеру с вашей карты. Попробуйте выбрать другой способ оплаты заказа.</p>

<p><strong>6. Можете ли вы дать гарантию того, что оплата картой безопасна?</strong></p>

<p>Да, мы гарантируем безопасность для наших клиентов. Оплата выполняется на отдельной защищенной страничке, а данные карты после оплаты не сохраняются в системе. Мы сотрудничаем с надеждой и известной платежной системой CloudPayments.<span id="nal" name="nal"></span></p>

<p>&nbsp;</p>

<p><strong>7. Можно ли расплачиваться наличными?</strong><span id="vozvrat" name="vozvrat"></span></p>

<p>На сегодняшний день нельзя. В ближайшее время оплата доставки цветов наличными станет доступна нашим клиентам, тогда после заполнения необходимых данных по заказу вам будет предложено несколько способов оплаты на ваш выбор.</p>

<p><strong>8. Как происходит возврат денег?</strong><span id="nam" name="nam"></span></p>

<p>В случае отмены заказа, оплаченная сумма вернется в полном объеме на ваш банковский счет в течение 7 рабочих дней. Если ваш заказ еще не был принят флористами или был сделан через приложение для мобильного телефона, то деньги вернутся в течение нескольких секунд, так как они были всего лишь заморожены.</p>

<p><strong>9. Есть ли гарантии, что оплаченная сумма попадет к вам?</strong><span id="sposob2" name="sposob2"></span></p>

<p>Оплата заказа банковской картой проходит мгновенно. Мы сотрудничаем с системой CloudPayments, которая является проверенным способом оплаты. После совершения платежа вам придет сообщение о том, что операция успешно выполнена. Вы также можете удостовериться в этом в личном кабинете.</p>

<p><strong>10. Какими способами можно оплатить заказ?</strong></p>

<p>Оплатить заказ можно прямо на сайте через банковскую карту или при получении наличными. Юридические лица могут воспользоваться услугой безналичного расчета.<span id="dostavka" name="dostavka"></span></p>

