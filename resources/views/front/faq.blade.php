<p><strong>1. Каков порядок оформления заказа?</strong></p><span id="slejka" name="slejka"></span>

<p>Сначала вы указываете город доставки, выбираете понравившийся букет нажатием кнопки на него и переходите на страницу оформления заказа. Здесь вы указываете данные получателя, время доставки и данные отправителя букета. После успешной оплаты заказ переходит к флористам. О доставке вы будете осведомлены сообщением, отправленным на указанный вами номер телефона и email, а также, информация доступна в личном кабинете на floristum.ru.</p>

<p><strong>2. Как я могу отследить статус заказа?</strong></p><span id="sposobi" name="sposobi"></span>

<p>Последнюю информацию о статусе вашего заказа мы отправляем СМС-сообщениями, электронными письмами, также можно проверить статус заказа в личном кабинете на нашем сайте. Вы получите смс и email сообщение о получении оплаты заказа и контакты флориста, собирающего ваш букет, вы можете в любой момент связаться с флористом, уточнить статус или внести коррективы в параметры заказа цветов.</p>

<p><strong>3. Какими способами я могу оплатить заказ?</strong><span id="floristum" name="floristum"></span></p>

<p>Произвести оплату можно прямо на сайте с помощью банковской карты для физического лица. Для юридических лиц доступна оплата по безналичному расчету.</p>

<p><strong>4. Что такое Floristum.ru?</strong></p>

<p>Floristum.ru &ndash; это удобный сервис, где цветочные магазины и отдельные флористы со всех уголков мира размещают свои товары на продажу. Здесь вы можете подобрать и заказать букет у флориста из вашего города, который полностью удовлетворит ваши желания. Помимо этого на сайте размещают отзывы реальные клиенты, что очень удобно при выборе места для оформления заказа доставки цветов. Floristum.ru гарантирует вам исполнение заказа, в непредвиденных ситуациях сервис вернет вам ваши деньги, это возможно благодаря тому, что флористы и магазины получают деньги за доставку цветов только спустя трое суток после выполнения заказа, в этот период клиент может обратиться с жалобой и получить деньги обратно.</p><span id="oplata" name="oplata"></span>

