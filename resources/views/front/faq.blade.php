<p><strong>Доставка</strong></p>

<p><strong>Возможна ли быстрая доставка?</strong></p>

<p>Рядом с каждым букетом стоит время, которое будет затрачено на оформление и доставку цветов. На сайте есть удобный фильтр &laquo;Быстрая доставка&raquo;, применив его, вы увидите варианты букетов, которые могут быть доставлены в короткий период времени.</p>

<p><strong>Какова стоимость доставки?</strong></p>

<p>Доставка по городу бесплатная, в столице и Спб&ndash; в пределах кольцевой автодороги. Если адрес доставки находится за пределами города, то ее стоимость рассчитывается автоматически. Система, опираясь на расстояние в километрах, выдаст вам цену доставки. Обычно оценивается в 45 рублей за 1 км.</p>

<p>В случае если вы не можете указать точный адрес доставки, а непосредственно при отправке букета получатель будет находиться за пределами города, то вам обязательно позвонит сотрудник для урегулирования этого вопроса.</p>

<p><strong>Можно произвести доставку к точно назначенному времени?</strong></p>

<p>На сайте вы можете указать часовой период доставки. К сожалению, наш сервис не производит доставку к точному времени. Но мы стараемся найти индивидуальный подход к каждому клиенту, поэтому вы можете указать свои пожелания к заказу, а наши сотрудники постараются сделать все возможное для их осуществления.</p>

