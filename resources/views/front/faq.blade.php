<p><strong>Как меня осведомят о доставке?</strong></p>

<p>Оформив заказ, вы получите сообщение с уникальной ссылкой для проверки статуса вашего заказа. Перейдя по ней, вы сможете общаться с флористами, увидеть фотографии готового букета и отслеживать местонахождение вашего заказа на карте. По окончанию доставки вы можете написать отзыв флористу, с которым вы сотрудничали. Мы рады, когда клиенты идут на обратную связь, поэтому вы можете отправить нам сообщение-отзыв с общими впечатлениями о заказе доставки цветов.</p>

<p><strong>Могу ли я оформить заказ доставки в другую страну?</strong></p>

<p>Да, с помощью нашего сервиса вы можете осуществить доставку цветов не только по стране, но и по всему миру. Просто вбейте интересующий вас город в поиск и вам будут показаны все доступные варианты букетов.</p>

