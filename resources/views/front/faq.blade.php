<p>Да, можно. Если вам хочется поменять что-то в выбранном букете, то просто оставьте свои пожелания в дополнительной информации во время оформления заказа. Наши флористы созвонятся с вами для обсуждения возможных вариантов замены.</p>

<p><strong>28. Где можно найти состав букета?</strong><span id="size" name="size"></span></p>

<p>Возле картинки с букетом есть полный разбор его состава. Также вы можете кликнуть на любое название цветка, он выделится цветом на картинке.</p>

<p><strong>29. Как я могу узнать размеры букета?</strong><span id="super" name="super"></span></p>

<p>Рядом с каждой фотографией размещены размеры букета. Они указываются флористом, который является автором цветочной композиции.</p>

<p><strong>30. Что я могу найти в разделе &laquo;Суперпредложение&raquo;?</strong><span id="opredelen" name="opredelen"></span></p>

<p>Здесь размещены предложения от оптово-розничных магазинов с выгодными ценами на букеты. Кроме того, вы можете лично определиться с количеством цветов, типом, длиной стебля и цветом.</p>

<p><strong>31. Что делать, если нужен букет с определенными цветами?</strong><span id="kartinka" name="kartinka"></span></p>

<p>Для поиска букетов с нужными вам цветами обратитесь к помощи фильтров.</p>

<p><strong>32. Будет ли букет таким же, как на картинке с сайта?<span id="bbolshe" name="bbolshe"></span></strong></p>

<p>Да, конечно. Флористы выкладывают фотографии букетов, которые они уже собирали ранее, поэтому воспроизвести такую же композицию им будет легко.</p>

<p><strong>33. Можно ли сделать букет больше?</strong><span id="gorshki" name="gorshki"></span></p>

<p>Да, перейдя на страничку товара можно увеличить букет на 30% или на 60%.То есть цена букета станет выше на эту часть от первоначальной суммы, а в состав композиции будут добавлены цветы, входящие в букет. Если вы выбираете букет их одного вида цветов, то вы можете увеличить их количество на любое число.</p>

<p><strong>34. Можно ли приобрести цветы в горшках или для рассады?</strong><span id="jelanie" name="jelanie"></span></p>

<p>Все варианты букетов представлены на сайте. Для поиска мы рекомендуем воспользоваться удобными фильтрами. Но в основном мы сотрудничаем с флористами, которые работают со срезанными цветами.</p>

<p><strong>35. Где можно разместить мои пожелания к заказу?</strong><span id="fresh" name="fresh"></span></p>

<p>Указывайте ваши пожелания и условия к выполнению заказа в дополнительной информации. При их наличии с вами обязательно созвонится флорист для обсуждения.</p>

<p><strong>36. Цветы будут свежими?</strong></p>

<p>Флористы, сотрудничающие с сервисом Floristum.ru, знают наше правило: &laquo;Только свежие цветы! Не согласен? Floristum.ru не для вас&raquo;. Поэтому вы можете быть уверены, что в состав композиций входят только свежие цветы. При доставке букета получатель дает оценку свежести цветов. В конце концов, на каждого интересующего вас флориста вы можете найти отзывы на нашем сайте.<span id="garantii" name="garantii"></span></p>

