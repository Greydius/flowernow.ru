<p>Возле каждого букета стоит время, которое необходимо для оформления и доставки букета до адресата. При оформлении заказа обязательно ориентируйтесь на него. Если вам нужно доставить цветы срочно, то воспользуйтесь фильтром &laquo;Быстрая доставка&raquo;. Если вам необходима доставка на определенную дату, то выберите ее при оформлении заказа. Если вы поставите галочку возле слова &laquo;Напомнить&raquo; в личном кабинете, то сервис Floristum.ru напомнит вам о заказе. Письмо придет вам за три дня до назначенного дня доставки.</p>

