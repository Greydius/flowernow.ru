<p><strong>18. Как оформить заказ?</strong><span id="ne" name="ne"></span></p>

<p>Просто укажите место доставки, выберите понравившийся букет и оформите заказ. В заказе укажите контактные данные отправителя и получателя, а также время доставки. После успешной оплаты заказа наши флористы примут его в работу. Вы будете уведомлены о доставке с помощью сообщения.</p>

<p><strong>19. Что делать, если букет не понравится?</strong><span id="nesvyazatsa" name="nesvyazatsa"></span></p>

<p>Если полученные цветы не понравятся вам по каким-либо причинам, то зайдите в личный кабинет или перейдите по ссылке, отправленной вам в сообщении или почтой. Там вы сможете оставить отзыв о вашем заказе. Если отзыв будет негативным, то вы сможете открыть спор, тогда денежная сумма на счету флориста будет заморожена на время разбирательства. Согласно политике нашего сервиса, клиент может договориться с флористом на замену букета или на возврат суммы в полном объеме. Также флористы имеют право предоставить вам скидку на следующие заказы. Спор может быть открыт в течение трех дней после доставки букета. Если вы раньше писали магазину или отдельному флористу хороший отзыв по заказу, то спор не откроется.</p>

<p><strong>20. Не могу связаться с флористами по телефону. Что делать?</strong><span id="ktozvonit" name="ktozvonit"></span></p>

<p>Вы можете кликнуть на кнопку &laquo;Не могу дозвониться до магазина&raquo;. В этом случае флорист получит оповещение о том, что вы пытались дозвониться, а рейтинг магазина будет автоматически уменьшен. В свою очередь мы выйдем на контакт с магазином, и его сотрудники обязательно вам перезвонят.</p>

<p><strong>21. Если о времени доставки нужно договориться с получателем, то когда ему позвонят?</strong><span id="zaranee" name="zaranee"></span></p>

<p>Обычно с получателями связываются перед непосредственным осуществлением доставки. Курьер согласовывает удобное время для того, чтобы доставить свежие цветы по адресу. Если вы оформили заказ на определенную дату, то с получателем созвонятся в день доставки, обычно это происходит в первой половине дня. В личном кабинете вы сможете узнать время доставки вашего заказа, оно будет поменяно на удобное время для получателя.</p>

<p><strong>22. Нужно ли оформлять заказ заранее?</strong><span id="korp" name="korp"></span></p>

<p>Для каждого букета определено минимальное время для его оформления и доставки, оно указано рядом с каждым букетом. У вас есть возможность оформить заказ как заранее, так и в день доставки.</p>

<p><strong>23. Как оформляется корпоративный заказ?</strong><span id="dogovor" name="dogovor"></span></p>

<p>Наш сервис активно сотрудничает с юридическими лицами и регулярно выполняет корпоративные заказы, в этом нам помогает автоматизированная система. Огромный ассортимент цветов позволяет выбрать букеты для любых случаев. Оформляя заказ, вы должны указать реквизиты юридического лица. Счет будет автоматически отправлен на вашу почту.</p>

