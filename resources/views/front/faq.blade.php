<p><strong>Доставка</strong></p>

<p><strong>11. Возможна ли быстрая доставка?</strong><span id="stoimost" name="stoimost"></span></p>

<p>Рядом с каждым букетом стоит время, которое будет затрачено на оформление и доставку цветов. На сайте есть удобный фильтр &laquo;Быстрая доставка&raquo;, применив его, вы увидите варианты букетов, которые могут быть доставлены в короткий период времени.</p>

<p><strong>12. Какова стоимость доставки?</strong></p>

<p>Доставка по городу бесплатная, в столице и Спб&ndash; в пределах кольцевой автодороги. Если адрес доставки находится за пределами города, то ее стоимость рассчитывается автоматически. Система, опираясь на расстояние в километрах, выдаст вам цену доставки. Обычно оценивается в 45 рублей за 1 км.<span id="tochno" name="tochno"></span></p>

<p>В случае если вы не можете указать точный адрес доставки, а непосредственно при отправке букета получатель будет находиться за пределами города, то вам обязательно позвонит сотрудник для урегулирования этого вопроса.</p>

<p><strong>13. Можно произвести доставку к точно назначенному времени?</strong></p>

<p>На сайте вы можете указать часовой период доставки. К сожалению, наш сервис не производит доставку к точному времени. Но мы стараемся найти индивидуальный подход к каждому клиенту, поэтому вы можете указать свои пожелания к заказу, а наши сотрудники постараются сделать все возможное для их осуществления.<span id="znayu" name="znayu"></span></p>

<p>&nbsp;</p>

<p><strong>14. Могу ли я сделать заказ, если я не знаю адрес получателя?</strong><span id="osvedom" name="osvedom"></span></p>

<p>Конечно, просто оставьте нам номер телефона получателя. Курьер созвонится с ним и уточнит время и место доставки вашего заказа.</p>

<p><strong>15. Как меня осведомят о доставке?</strong><span id="drugaya" name="drugaya"></span></p>

<p>Оформив заказ, вы получите сообщение с уникальной ссылкой для проверки статуса вашего заказа. Перейдя по ней, вы сможете общаться с флористами, увидеть фотографии готового букета и отслеживать местонахождение вашего заказа на карте. По окончанию доставки вы можете написать отзыв флористу, с которым вы сотрудничали. Мы рады, когда клиенты идут на обратную связь, поэтому вы можете отправить нам сообщение-отзыв с общими впечатлениями о заказе доставки цветов.</p>

<p><strong>16. Могу ли я оформить заказ доставки в другую страну?</strong><span id="when" name="when"></span></p>

<p>Да, с помощью нашего сервиса вы можете осуществить доставку цветов не только по стране, но и по всему миру. Просто вбейте интересующий вас город в поиск и вам будут показаны все доступные варианты букетов.</p>

<p><strong>17. Когда доставят заказ?</strong><span id="kak" name="kak"></span></p>

<p>Возле каждого букета стоит время, которое необходимо для оформления и доставки букета до адресата. При оформлении заказа обязательно ориентируйтесь на него. Если вам нужно доставить цветы срочно, то воспользуйтесь фильтром &laquo;Быстрая доставка&raquo;. Если вам необходима доставка на определенную дату, то выберите ее при оформлении заказа. Если вы поставите галочку возле слова &laquo;Напомнить&raquo; в личном кабинете, то сервис Floristum.ru напомнит вам о заказе. Письмо придет вам за три дня до назначенного дня доставки.</p>

